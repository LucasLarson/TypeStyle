  func styledText(forText text: String, rowIndex: Int) -> String {
    let newStyle = styles[rowIndex]

    let convert = Dictionary(uniqueKeysWithValues: zip(base, newStyle))

    let newText = String(text.map {
      convert[$0] ?? $0
    })

    return newText
  }

}
